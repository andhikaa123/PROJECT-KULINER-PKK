# Operator Aritmatika
a = 10
b = 5
penjumlahan = a + b
pengurangan = a - b
perkalian = a * b
pembagian = a / b
modulus = a % b
pangkat = a ** b

print("Hasil Operator Aritmatika:")
print("Penjumlahan:", penjumlahan)
print("Pengurangan:", pengurangan)
print("Perkalian:", perkalian)
print("Pembagian:", pembagian)
print("Modulus:", modulus)
print("Pangkat:", pangkat)

# Operator Pembanding
x = 15
y = 20
sama_dengan = x == y
tidak_sama_dengan = x != y
lebih_besar = x > y
lebih_kecil = x < y
lebih_besar_sama_dengan = x >= y
lebih_kecil_sama_dengan = x <= y

print("\nHasil Operator Pembanding:")
print("Sama dengan:", sama_dengan)
print("Tidak sama dengan:", tidak_sama_dengan)
print("Lebih besar:", lebih_besar)
print("Lebih kecil:", lebih_kecil)
print("Lebih besar sama dengan:", lebih_besar_sama_dengan)
print("Lebih kecil sama dengan:", lebih_kecil_sama_dengan)

# Operator Logika
p = True
q = False
logika_and = p and q
logika_or = p or q
logika_not_p = not p

print("\nHasil Operator Logika:")
print("Logika AND:", logika_and)
print("Logika OR:", logika_or)
print("Logika NOT (p):", logika_not_p)

# Operator Bitwise
x = 5  # 0b0101
y = 3  # 0b0011
bitwise_and = x & y
bitwise_or = x | y
bitwise_xor = x ^ y
bitwise_komplemen = ~x
geser_kanan = x >> 1
geser_kiri = x << 1

print("\nHasil Operator Bitwise:")
print("Bitwise AND:", bitwise_and)
print("Bitwise OR:", bitwise_or)
print("Bitwise XOR:", bitwise_xor)
print("Bitwise Komplemen:", bitwise_komplemen)
print("Geser Kanan:", geser_kanan)
print("Geser Kiri:", geser_kiri)